package predavanje02;

public class Operatori {
	
	public static void main(String[] args) {
		
		
int i=4,j=8;
		
		int z = i+j;
		System.out.println(z);
		System.out.println(i+j);
		
		z=i-j;
		z=i*j;
		z=i/j;
		System.out.println(z);
		
		float rez=i/j;
		System.out.println(rez);
		
		rez=(float)i/j;
		System.out.println(rez);
		
		System.out.println(10/3);
		
		System.out.println(10%3);
		
		
	}

}
